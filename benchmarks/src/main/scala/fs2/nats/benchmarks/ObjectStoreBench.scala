package fs2.nats.benchmarks

import cats.effect.{ExitCode, IO, IOApp}
import com.comcast.ip4s.{Host, Port}
import fs2.Stream
import fs2.nats.client.{ClientConfig, NatsClient, SlowConsumerPolicy}
import fs2.nats.objectstore.*
import scala.concurrent.duration.*

/** End-to-end Object Store throughput harness against a live, JetStream-enabled
  * NATS server.
  *
  * Measures large-object streaming put and get throughput (MB/s) at a given
  * chunk size, driving the object from/through a generated `Stream[IO, Byte]`
  * so neither side materializes the whole object — the residency stays bounded
  * by the chunk size and the publish window.
  *
  * Prerequisites: a JetStream-enabled NATS server, e.g. `docker compose up -d`.
  *
  * Run:
  * {{{
  *   sbt "benchmarks/runMain fs2.nats.benchmarks.ObjectStoreBench [sizeMB] [chunkKiB] [host] [port]"
  *   # defaults: 64 128 localhost 4222
  * }}}
  */
object ObjectStoreBench extends IOApp:

  override def run(args: List[String]): IO[ExitCode] =
    val sizeMB = args.lift(0).flatMap(_.toIntOption).getOrElse(64)
    val chunkKiB = args.lift(1).flatMap(_.toIntOption).getOrElse(128)
    val host = args.lift(2).getOrElse("localhost")
    val port = args.lift(3).flatMap(_.toIntOption).getOrElse(4222)

    val config = ClientConfig(
      host = Host.fromString(host).get,
      port = Port.fromInt(port).get,
      queueCapacity = 1 << 16,
      slowConsumerPolicy = SlowConsumerPolicy.Block
    )
    val totalBytes = sizeMB.toLong * 1024 * 1024
    val chunkSize = chunkKiB * 1024

    NatsClient.connect[IO](config).use { client =>
      client.jetStream().use { js =>
        js.createObjectStore(ObjConfig("OBJBENCH")).flatMap { os =>
          (for
            _ <- IO.println(
              s"fs2-nats Object Store bench -> $host:$port (size=${sizeMB}MB, chunk=${chunkKiB}KiB)"
            )
            // PUT
            p0 <- IO.monotonic
            info <- os.put(
              ObjectMeta("blob", maxChunkSize = chunkSize),
              source(totalBytes)
            )
            p1 <- IO.monotonic
            _ <- reportRate("put", totalBytes, p1 - p0)
            _ <- IO.println(s"  stored ${info.chunks} chunks, ${info.digest}")
            // GET (stream + digest-verify, count bytes without materializing)
            g0 <- IO.monotonic
            n <- os.get("blob").flatMap(_.get.data.compile.count)
            g1 <- IO.monotonic
            _ <- reportRate("get", n, g1 - g0)
          yield ()).guarantee(js.deleteObjectStore("OBJBENCH").attempt.void)
        } *> IO.pure(ExitCode.Success)
      }
    }

  /** A generated byte source of exactly `total` bytes, emitted in 64 KiB
    * chunks so the generator itself never holds the whole object.
    */
  private def source(total: Long): Stream[IO, Byte] =
    val unit = 64 * 1024
    val block = fs2.Chunk.array(Array.tabulate(unit)(i => (i % 256).toByte))
    def go(remaining: Long): Stream[IO, Byte] =
      if remaining <= 0 then Stream.empty
      else if remaining >= unit then
        Stream.chunk(block) ++ go(remaining - unit)
      else Stream.chunk(block.take(remaining.toInt))
    go(total)

  private def reportRate(
      label: String,
      bytes: Long,
      elapsed: FiniteDuration
  ): IO[Unit] =
    val secs = elapsed.toNanos / 1e9
    val mb = bytes.toDouble / (1024 * 1024) / secs
    IO.println(
      f"[$label] ${bytes.toDouble / (1024 * 1024)}%.1f MB in $secs%.3f s => $mb%.1f MB/s"
    )
